const generatePhotos = (orderId: string): OrderPhoto[] => {
  const photos: OrderPhoto[] = [];
  const timestamp = now.toISOString();

  photos.push({
    id: `${orderId}-before-1`,
    type: 'before',
    url: `https://picsum.photos/seed/${orderId}b1/400/300`,
    uploadedAt: timestamp,
  });

  photos.push({
    id: `${orderId}-after-1`,
    type: 'after',
    url: `https://picsum.photos/seed/${orderId}a1/400/300`,
    uploadedAt: timestamp,
  });

  return photos;
};

